export default async function handler(req, res) {
  if (req.method !== "GET") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const key = process.env.MAERSK_CONSUMER_KEY;
  if (!key) {
    return res.status(500).json({ error: "MAERSK_CONSUMER_KEY not set in .env.local" });
  }

  const { locationType = "TERMINAL", countryCode, cityName, UNLocationCode } = req.query;

  const params = new URLSearchParams({ locationType });
  if (countryCode)    params.set("countryCode",    countryCode);
  if (cityName)       params.set("cityName",        cityName);
  if (UNLocationCode) params.set("UNLocationCode",  UNLocationCode);

  try {
    const upstream = await fetch(
      `https://api.maersk.com/reference-data/locations?${params.toString()}`,
      {
        headers: {
          "Consumer-Key": key,
          "Accept":       "application/json",
        },
      }
    );

    if (!upstream.ok) {
      const text = await upstream.text();
      console.error("[/api/locations] Maersk error:", upstream.status, text);
      return res.status(upstream.status).json({ error: "Maersk API error", detail: text });
    }

    const raw = await upstream.json();
    const arr = Array.isArray(raw) ? raw : raw.locations ?? [];

    const locations = arr
      .filter((loc) => COORDS[loc.UNLocationCode])
      .map((loc) => {
        const [lat, lng] = COORDS[loc.UNLocationCode];
        return {
          name:        loc.locationName ?? loc.cityName ?? "Unknown",
          unlocode:    loc.UNLocationCode,
          lat,
          lng,
          type:        loc.locationType,
          countryCode: loc.countryCode,
        };
      });

    // Deduplicate by unlocode (keep first occurrence = most important terminal per port)
    const seen = new Set();
    const deduped = locations.filter((l) => {
      if (seen.has(l.unlocode)) return false;
      seen.add(l.unlocode);
      return true;
    });

    console.log(`[/api/locations] ${arr.length} Maersk terminals → ${deduped.length} matched & deduped`);

    res.setHeader("Cache-Control", "public, s-maxage=3600, stale-while-revalidate=7200");
    return res.status(200).json(deduped);

  } catch (err) {
    console.error("[/api/locations] Error:", err.message);
    return res.status(500).json({ error: err.message });
  }
}